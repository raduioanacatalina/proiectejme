package model;

public class Furnizor {

	
	private String denumire;
	private Integer id;
	private String numar_strada;
	private String nume_strada;
	private String oras;
	private String telefon;
	
	
	
	public String getDenumire() {
		return denumire;
	}
	public void setDenumire(String denumire) {
		this.denumire = denumire;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNumar_strada() {
		return numar_strada;
	}
	public void setNumar_strada(String numar_strada) {
		this.numar_strada = numar_strada;
	}
	public String getNume_strada() {
		return nume_strada;
	}
	public void setNume_strada(String nume_strada) {
		this.nume_strada = nume_strada;
	}
	public String getOras() {
		return oras;
	}
	public void setOras(String oras) {
		this.oras = oras;
	}
	public String getTelefon() {
		return telefon;
	}
	public void setTelefon(String telefon) {
		this.telefon = telefon;
	}

	
	

}
