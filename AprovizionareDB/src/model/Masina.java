package model;

public class Masina {

	private Integer id;
	private String marca;
	private String model;
	private String capacitate;
	private String nr_inmatriculare;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getCapacitate() {
		return capacitate;
	}
	public void setCapacitate(String capacitate) {
		this.capacitate = capacitate;
	}
	public String getNr_inmatriculare() {
		return nr_inmatriculare;
	}
	public void setNr_inmatriculare(String nr_inmatriculare) {
		this.nr_inmatriculare = nr_inmatriculare;
	}
	
	
}
