package model;

public class TotalVanzare {

	private Integer idMagazin;
	private Integer totalVanzari;
	private String denumireMagazin;
	
	public Integer getIdMagazin() {
		return idMagazin;
	}
	public void setIdMagazin(Integer idMagazin) {
		this.idMagazin = idMagazin;
	}
	public Integer getTotalVanzari() {
		return totalVanzari;
	}
	public void setTotalVanzari(Integer totalVanzari) {
		this.totalVanzari = totalVanzari;
	}
	public String getDenumireMagazin() {
		return denumireMagazin;
	}
	public void setDenumireMagazin(String denumireMagazin) {
		this.denumireMagazin = denumireMagazin;
	}
	
	
	
}
